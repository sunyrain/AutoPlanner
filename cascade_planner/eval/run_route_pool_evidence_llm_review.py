"""Run LLM review over route-pool evidence prompts.

This is an offline reviewer runner, separate from AUTOPLANNRELLM route search.
It reads prompt JSONL generated by ``build_route_pool_evidence_review_prompts``
and writes response JSONL compatible with
``ingest_route_pool_evidence_review_results``.
"""
from __future__ import annotations

import argparse
import concurrent.futures
import json
import os
import time
import urllib.error
from pathlib import Path
from typing import Any

from AUTOPLANNRELLM.deepseek_client import (
    DeepSeekJSONClient,
    is_placeholder_deepseek_key,
    normalize_deepseek_key_value,
)


SCHEMA_VERSION = "route_pool_evidence_llm_review_run.v1"

SYSTEM_PROMPT = (
    "You are a chemistry reviewer for chemoenzymatic retrosynthesis route blocks. "
    "Return only a JSON object matching the requested schema. Do not include markdown."
)


def run_route_pool_evidence_llm_review(
    *,
    prompts_jsonl: Path,
    output_jsonl: Path,
    report_json: Path,
    cache_path: Path | None = None,
    model: str | None = None,
    max_rows: int | None = None,
    start_index: int = 0,
    dry_run: bool = True,
    resume: bool = False,
    continue_on_error: bool = True,
    workers: int = 1,
    sleep_s: float = 0.0,
) -> dict[str, Any]:
    prompts = _read_jsonl(prompts_jsonl)
    if not dry_run:
        _require_real_review_key()
    completed_ids = _completed_review_ids(output_jsonl) if resume else set()
    selected = [
        (idx, row)
        for idx, row in enumerate(prompts[int(start_index) :], start=int(start_index))
        if str(row.get("review_id") or "") not in completed_ids
    ]
    if max_rows is not None:
        selected = selected[: max(0, int(max_rows))]
    output_jsonl.parent.mkdir(parents=True, exist_ok=True)
    rows = []
    started = time.monotonic()
    mode = "a" if resume and output_jsonl.exists() else "w"
    errors = []
    worker_count = max(1, int(workers or 1))
    with output_jsonl.open(mode, encoding="utf-8") as fh:
        if worker_count == 1:
            for offset, (prompt_index, prompt_row) in enumerate(selected):
                row, error = _run_one(
                    prompt_index=prompt_index,
                    prompt_row=prompt_row,
                    dry_run=dry_run,
                    model=model,
                    cache_path=cache_path,
                    continue_on_error=continue_on_error,
                )
                if error:
                    errors.append(error)
                _write_response_row(fh, row)
                rows.append(row)
                if sleep_s > 0 and offset + 1 < len(selected):
                    time.sleep(float(sleep_s))
        else:
            with concurrent.futures.ThreadPoolExecutor(max_workers=worker_count) as executor:
                futures = [
                    executor.submit(
                        _run_one,
                        prompt_index=prompt_index,
                        prompt_row=prompt_row,
                        dry_run=dry_run,
                        model=model,
                        cache_path=cache_path,
                        continue_on_error=continue_on_error,
                    )
                    for prompt_index, prompt_row in selected
                ]
                for future in concurrent.futures.as_completed(futures):
                    row, error = future.result()
                    if error:
                        errors.append(error)
                    _write_response_row(fh, row)
                    rows.append(row)
    report = {
        "schema_version": SCHEMA_VERSION,
        "generated_at": time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime()),
        "metadata": {
            "prompts_jsonl": str(prompts_jsonl),
            "output_jsonl": str(output_jsonl),
            "report_json": str(report_json),
            "cache_path": str(cache_path) if cache_path else None,
            "model": model or os.environ.get("DEEPSEEK_MODEL"),
            "start_index": start_index,
            "max_rows": max_rows,
            "dry_run": dry_run,
            "resume": resume,
            "continue_on_error": continue_on_error,
            "workers": worker_count,
            "sleep_s": sleep_s,
            "elapsed_s": round(time.monotonic() - started, 3),
        },
        "summary": {
            "prompt_rows_total": len(prompts),
            "completed_rows_before_run": len(completed_ids),
            "attempted_rows": len(selected),
            "written_rows": len(rows),
            "error_rows": len(errors),
            "dry_run": bool(dry_run),
        },
        "errors": errors[:50],
        "outputs": {"responses_jsonl": str(output_jsonl)},
    }
    report_json.parent.mkdir(parents=True, exist_ok=True)
    report_json.write_text(json.dumps(report, indent=2, ensure_ascii=False), encoding="utf-8")
    report_json.with_suffix(".md").write_text(_markdown(report), encoding="utf-8")
    return report


def _require_real_review_key() -> None:
    key = normalize_deepseek_key_value(os.environ.get("DEEPSEEK_API_KEY"))
    if not key:
        raise RuntimeError("DEEPSEEK_API_KEY is required when --no-dry-run is used")
    if is_placeholder_deepseek_key(key):
        raise RuntimeError(
            "DEEPSEEK_API_KEY still contains the dotenv placeholder; replace it before --no-dry-run"
        )
    os.environ["DEEPSEEK_API_KEY"] = key


def _run_one(
    *,
    prompt_index: int,
    prompt_row: dict[str, Any],
    dry_run: bool,
    model: str | None,
    cache_path: Path | None,
    continue_on_error: bool,
) -> tuple[dict[str, Any], dict[str, Any] | None]:
    error = None
    try:
        if dry_run:
            response = _dry_run_response(prompt_row)
        else:
            client = DeepSeekJSONClient(model=model, cache_path=cache_path)
            response = _call_client(client, prompt_row)
    except Exception as exc:
        error = {"prompt_index": prompt_index, "review_id": prompt_row.get("review_id"), **_error_payload(exc)}
        if not continue_on_error:
            raise
        response = _error_response(prompt_row, error)
    row = {
        "schema_version": SCHEMA_VERSION,
        "generated_at": time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime()),
        "prompt_index": prompt_index,
        "review_id": prompt_row.get("review_id"),
        "source_pool": prompt_row.get("source_pool"),
        "evidence_class": prompt_row.get("evidence_class"),
        "dry_run": bool(dry_run),
        "model": model or os.environ.get("DEEPSEEK_MODEL") or "deepseek-default",
        "response": response,
    }
    if error:
        row["error"] = error
    return row, error


def _write_response_row(fh: Any, row: dict[str, Any]) -> None:
    fh.write(json.dumps(row, ensure_ascii=False, sort_keys=True) + "\n")
    fh.flush()


def _call_client(client: DeepSeekJSONClient | None, prompt_row: dict[str, Any]) -> dict[str, Any]:
    if client is None:
        raise RuntimeError("client is not initialized")
    user_payload = {
        "prompt": prompt_row.get("prompt"),
        "review_id": prompt_row.get("review_id"),
        "expected_output_schema": prompt_row.get("expected_output_schema"),
    }
    response = client.request_json(
        task="route_pool_evidence_review",
        system=SYSTEM_PROMPT,
        user_payload=user_payload,
        max_tokens=900,
        temperature=0.0,
    )
    if not response.get("review_id"):
        response["review_id"] = prompt_row.get("review_id")
    return response


def _dry_run_response(prompt_row: dict[str, Any]) -> dict[str, Any]:
    return {
        "review_id": prompt_row.get("review_id"),
        "route_plausible": "unclear",
        "block_transform_correct": "unclear",
        "support_precedent_relevant": "unclear",
        "cascade_coherent": "unclear",
        "priority": "low",
        "risk_tags": ["other"],
        "rationale": "Dry-run placeholder response; no chemistry judgment was performed.",
    }


def _error_response(prompt_row: dict[str, Any], error: dict[str, Any]) -> dict[str, Any]:
    return {
        "review_id": prompt_row.get("review_id"),
        "route_plausible": "unclear",
        "block_transform_correct": "unclear",
        "support_precedent_relevant": "unclear",
        "cascade_coherent": "unclear",
        "priority": "low",
        "risk_tags": ["other"],
        "rationale": f"LLM review failed: {error.get('type')}. This is not a chemistry judgment.",
    }


def _error_payload(exc: Exception) -> dict[str, Any]:
    payload = {"type": exc.__class__.__name__, "message": str(exc)[:500]}
    if isinstance(exc, urllib.error.HTTPError):
        payload["status"] = exc.code
    return payload


def _completed_review_ids(output_jsonl: Path) -> set[str]:
    if not output_jsonl.exists():
        return set()
    out = set()
    with output_jsonl.open("r", encoding="utf-8") as fh:
        for line in fh:
            if not line.strip():
                continue
            try:
                row = json.loads(line)
            except json.JSONDecodeError:
                continue
            rid = str(row.get("review_id") or "")
            if rid:
                out.add(rid)
    return out


def _read_jsonl(path: Path) -> list[dict[str, Any]]:
    rows = []
    with Path(path).open("r", encoding="utf-8") as fh:
        for line in fh:
            if line.strip():
                payload = json.loads(line)
                if isinstance(payload, dict):
                    rows.append(payload)
    return rows


def _markdown(report: dict[str, Any]) -> str:
    lines = [
        "# Route Pool Evidence LLM Review Run",
        "",
        "## Summary",
        "",
        "| Metric | Value |",
        "|---|---:|",
    ]
    for key, value in (report.get("summary") or {}).items():
        lines.append(f"| `{key}` | `{value}` |")
    lines.extend(
        [
            "",
            "## Contract",
            "",
            "- This runner is offline review only; it does not alter search.",
            "- Dry-run responses are placeholders and must not be treated as expert labels.",
            "- Real runs require `--no-dry-run` and `DEEPSEEK_API_KEY`.",
            "- `--resume` appends and skips review ids already present in the output JSONL.",
            "- `--workers` can parallelize independent review calls; output order is not guaranteed when workers > 1.",
            "",
        ]
    )
    return "\n".join(lines)


def main() -> None:
    parser = argparse.ArgumentParser(description="Run LLM review over route-pool evidence prompts")
    parser.add_argument("--prompts-jsonl", required=True)
    parser.add_argument("--output-jsonl", required=True)
    parser.add_argument("--report-json", required=True)
    parser.add_argument("--cache")
    parser.add_argument("--model")
    parser.add_argument("--max-rows", type=int)
    parser.add_argument("--start-index", type=int, default=0)
    parser.add_argument("--workers", type=int, default=1)
    parser.add_argument("--sleep-s", type=float, default=0.0)
    parser.add_argument("--no-dry-run", action="store_true")
    parser.add_argument("--resume", action="store_true")
    parser.add_argument("--fail-fast", action="store_true")
    args = parser.parse_args()
    report = run_route_pool_evidence_llm_review(
        prompts_jsonl=Path(args.prompts_jsonl),
        output_jsonl=Path(args.output_jsonl),
        report_json=Path(args.report_json),
        cache_path=Path(args.cache) if args.cache else None,
        model=args.model,
        max_rows=args.max_rows,
        start_index=args.start_index,
        dry_run=not args.no_dry_run,
        resume=bool(args.resume),
        continue_on_error=not bool(args.fail_fast),
        workers=int(args.workers),
        sleep_s=float(args.sleep_s),
    )
    print(json.dumps({"summary": report["summary"], "outputs": report["outputs"]}, indent=2, ensure_ascii=False))


if __name__ == "__main__":
    main()
