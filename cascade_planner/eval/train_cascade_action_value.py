@dataclass
class ActionValueDataset:
    rows: list[dict[str, Any]]
    x: np.ndarray
    y: np.ndarray
    groups: list[str]
    schema: dict[str, Any]


class CascadeActionValueNetwork(nn.Module):
    def __init__(self, input_dim: int, hidden: int = 192):
        super().__init__()
        self.net = nn.Sequential(
            nn.Linear(input_dim, hidden),
            nn.GELU(),
            nn.Dropout(0.12),
            nn.Linear(hidden, max(32, hidden // 2)),
            nn.GELU(),
            nn.Linear(max(32, hidden // 2), 1),
        )

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        return self.net(x).squeeze(-1)


def train_cascade_action_value(
    *,
    pack_dir: Path,
    model_output: Path,
    report_output: Path,
    md_output: Path | None = None,
    epochs: int = 20,
    batch_size: int = 512,
    lr: float = 1e-3,
    hidden: int = 192,
    n_bits: int = 128,
    seed: int = 42,
    device: str | None = None,
    loss_mode: str = "pairwise",
    selection_metric: str = "top1_positive_state_hit_rate",
    label_name: str = "action_value",
) -> dict[str, Any]:
    torch.manual_seed(seed)
    np.random.seed(seed)
    device = device or ("cuda" if torch.cuda.is_available() else "cpu")
    if loss_mode not in {"pairwise", "bce"}:
        raise ValueError(f"unsupported loss_mode: {loss_mode}")
    _validate_selection_metric(selection_metric)

    rows = _read_jsonl(pack_dir / "action_value.jsonl")
    dataset = build_dataset(rows, n_bits=n_bits, label_name=label_name)
    if len(dataset.rows) < 8:
        raise ValueError(f"not enough action-value rows to train: {len(dataset.rows)}")
    train_idx, val_idx, test_idx = _split_indices_by_role(dataset.rows)
    train_rows = [dataset.rows[idx] for idx in train_idx]
    val_rows = [dataset.rows[idx] for idx in val_idx]
    test_rows = [dataset.rows[idx] for idx in test_idx]
    x_train = torch.tensor(dataset.x[train_idx], dtype=torch.float32)
    y_train = torch.tensor(dataset.y[train_idx], dtype=torch.float32)
    x_val = torch.tensor(dataset.x[val_idx], dtype=torch.float32)
    y_val = torch.tensor(dataset.y[val_idx], dtype=torch.float32)
    x_test = torch.tensor(dataset.x[test_idx], dtype=torch.float32) if test_idx else None
    y_test = torch.tensor(dataset.y[test_idx], dtype=torch.float32) if test_idx else None

    pair_loader = None
    n_train_pairs = 0
    if loss_mode == "pairwise":
        pair_idx = _state_pair_indices(train_rows)
        n_train_pairs = len(pair_idx)
        if pair_idx:
            pos_x = torch.tensor(dataset.x[[train_idx[pos] for pos, _ in pair_idx]], dtype=torch.float32)
            neg_x = torch.tensor(dataset.x[[train_idx[neg] for _, neg in pair_idx]], dtype=torch.float32)
            weights = torch.tensor(
                [max(0.25, _action_value(train_rows[pos])) for pos, _ in pair_idx],
                dtype=torch.float32,
            )
            pair_loader = DataLoader(TensorDataset(pos_x, neg_x, weights), batch_size=batch_size, shuffle=True)

    loader = DataLoader(TensorDataset(x_train, y_train), batch_size=batch_size, shuffle=True)
    model = CascadeActionValueNetwork(dataset.x.shape[1], hidden=hidden).to(device)
    opt = torch.optim.AdamW(model.parameters(), lr=lr, weight_decay=1e-4)
    best_state = None
    best_selection_score = None
    best_loss = float("inf")
    best_epoch = None
    best_metrics = None
    history = []

    for epoch in range(max(1, epochs)):
        model.train()
        total = 0.0
        n_seen = 0
        active_loader = pair_loader if pair_loader is not None else loader
        for batch in active_loader:
            if pair_loader is not None:
                pos_x, neg_x, weights = batch
                pos_x = pos_x.to(device)
                neg_x = neg_x.to(device)
                weights = weights.to(device)
                loss_vec = nn.functional.softplus(-(model(pos_x) - model(neg_x)))
                loss = (loss_vec * weights).mean()
                seen = len(pos_x)
            else:
                xb, yb = batch
                xb = xb.to(device)
                yb = yb.to(device)
                logits = model(xb)
                weights = 1.0 + 4.0 * (yb > 0).float() + 3.0 * (yb >= 1.0).float()
                loss = (
                    nn.functional.binary_cross_entropy_with_logits(logits, yb, reduction="none")
                    * weights
                ).mean()
                seen = len(xb)
            opt.zero_grad()
            loss.backward()
            opt.step()
            total += float(loss.item()) * seen
            n_seen += seen

        val_loss, val_metrics = evaluate(model, x_val.to(device), y_val.to(device), val_rows)
        row = {
            "epoch": epoch + 1,
            "train_loss": round(total / max(n_seen, 1), 6),
            "val_loss": round(val_loss, 6),
            **val_metrics,
        }
        history.append(row)
        selection_score = _selection_score(selection_metric, val_loss, val_metrics)
        if _is_better_selection(selection_metric, selection_score, best_selection_score):
            best_selection_score = selection_score
            best_loss = val_loss
            best_epoch = epoch + 1
            best_metrics = dict(val_metrics)
            best_state = {key: value.detach().cpu().clone() for key, value in model.state_dict().items()}

    if best_state is not None:
        model.load_state_dict(best_state)
    final_loss, final_metrics = evaluate(model, x_val.to(device), y_val.to(device), val_rows)
    cost_baselines = evaluate_cost_baselines(val_rows)
    test_loss = None
    test_metrics = None
    test_cost_baselines = None
    if x_test is not None and y_test is not None and test_rows:
        test_loss, test_metrics = evaluate(model, x_test.to(device), y_test.to(device), test_rows)
        test_cost_baselines = evaluate_cost_baselines(test_rows)
    report = {
        "metadata": {
            "date": time.strftime("%Y-%m-%d"),
            "pack_dir": str(pack_dir),
            "model_output": str(model_output),
            "n_rows": len(dataset.rows),
            "n_train": len(train_idx),
            "n_val": len(val_idx),
            "n_test": len(test_idx),
            "hidden": hidden,
            "n_bits": n_bits,
            "device": device,
            "feature_schema": dataset.schema,
            "label_contract": "internal_search_action_value_not_record_gold.v1",
            "loss_mode": loss_mode,
            "selection_metric": selection_metric,
            "label_name": label_name,
            "n_train_pairs": n_train_pairs,
            "training_caution": (
                "Train on training-split expansion traces and evaluate on held-out targets. "
                "The model ranks actions inside ChemEnzy search states; it is not a record classifier."
            ),
        },
        "best_val_loss": round(float(best_loss), 6),
        "best_checkpoint": {
            "epoch": best_epoch,
            "selection_metric": selection_metric,
            "selection_score": round(float(best_selection_score), 6) if best_selection_score is not None else None,
            "metrics": best_metrics or {},
        },
        "final_val_loss": round(float(final_loss), 6),
        "final_metrics": final_metrics,
        "validation_metrics": final_metrics,
        "cost_baselines": cost_baselines,
        "final_test_loss": round(float(test_loss), 6) if test_loss is not None else None,
        "test_metrics": test_metrics,
        "test_cost_baselines": test_cost_baselines,
        "history": history,
    }
    model_output.parent.mkdir(parents=True, exist_ok=True)
    torch.save(
        {
            "state_dict": model.state_dict(),
            "model_class": "CascadeActionValueNetwork",
            "feature_schema": dataset.schema,
            "hidden": hidden,
            "pack_dir": str(pack_dir),
            "selection_metric": selection_metric,
        },
        model_output,
    )
    report_output.parent.mkdir(parents=True, exist_ok=True)
    report_output.write_text(json.dumps(report, indent=2, ensure_ascii=False), encoding="utf-8")
    if md_output is not None:
        md_output.parent.mkdir(parents=True, exist_ok=True)
        md_output.write_text(report_markdown(report), encoding="utf-8")
    return report


def build_dataset(rows: list[dict[str, Any]], *, n_bits: int = 128, label_name: str = "action_value") -> ActionValueDataset:
    kept = [row for row in rows if row.get("parent_mol") and row.get("reactants")]
    categories = {
        field: sorted({_feature_field(row, field) for row in kept})
        for field in CATEGORICAL_FIELDS
    }
    schema = {
        "schema_version": "cascade_action_value_features.v2",
        "feature_contract": "state_action_candidate_with_process_context.v2",
        "categorical_fields": CATEGORICAL_FIELDS,
        "categories": categories,
        "numeric_fields": NUMERIC_FIELDS,
        "component_fields": COMPONENT_FIELDS,
        "process_context_fields": PROCESS_CONTEXT_FIELDS,
        "n_bits": n_bits,
        "fingerprint_blocks": ["parent_morgan", "reactants_morgan"],
    }
    kept = [_with_selected_label(row, label_name) for row in kept]
    features = [_feature_vector(row, schema) for row in kept]
    labels = [_action_value(row) for row in kept]
    groups = [str(row.get("state_id") or "") for row in kept]
    schema["feature_dim"] = len(features[0]) if features else 0
    return ActionValueDataset(
        rows=kept,
        x=np.asarray(features, dtype=np.float32),
        y=np.asarray(labels, dtype=np.float32),
        groups=groups,
        schema=schema,
    )


def evaluate(
    model: nn.Module,
    x: torch.Tensor,
    y: torch.Tensor,
    rows: list[dict[str, Any]],
) -> tuple[float, dict[str, Any]]:
    model.eval()
    with torch.no_grad():
        logits = model(x)
        binary = (y > 0).float()
        loss = float(nn.functional.binary_cross_entropy_with_logits(logits, binary).item())
        scores = torch.sigmoid(logits).detach().cpu().numpy()
    return loss, _ranking_metrics(rows, scores)


def evaluate_cost_baselines(rows: list[dict[str, Any]]) -> dict[str, Any]:
    out = {}
    for field in ("base_cost", "total_cost"):
        scores = []
        for row in rows:
            cost = _float_or_none(row.get(field))
            scores.append(0.0 if cost is None else -float(cost))
        out[field] = _ranking_metrics(rows, np.asarray(scores, dtype=np.float32))
    return out


def _ranking_metrics(rows: list[dict[str, Any]], scores: np.ndarray) -> dict[str, Any]:
    by_state: dict[str, list[int]] = defaultdict(list)
    for idx, row in enumerate(rows):
        by_state[str(row.get("state_id") or idx)].append(idx)
    positive_states = 0
    exact_states = 0
    top1_positive = 0
    top5_positive = 0
    top1_exact = 0
    top5_exact = 0
    positive_pair_correct = 0.0
    positive_pair_total = 0
    exact_pair_correct = 0.0
    exact_pair_total = 0
    best_positive_ranks = []
    best_exact_ranks = []
    for indices in by_state.values():
        positive = [idx for idx in indices if _action_value(rows[idx]) > 0.0]
        exact = [idx for idx in indices if int((rows[idx].get("labels") or {}).get("exact_gt_reaction") or 0) > 0]
        ordered = sorted(indices, key=lambda idx: float(scores[idx]), reverse=True)
        if positive:
            positive_states += 1
            rank = min(ordered.index(idx) + 1 for idx in positive)
            best_positive_ranks.append(rank)
            top1_positive += int(rank <= 1)
            top5_positive += int(rank <= 5)
            correct, total = _pairwise_score_accuracy(indices, positive, scores)
            positive_pair_correct += correct
            positive_pair_total += total
        if exact:
            exact_states += 1
            rank = min(ordered.index(idx) + 1 for idx in exact)
            best_exact_ranks.append(rank)
            top1_exact += int(rank <= 1)
            top5_exact += int(rank <= 5)
            correct, total = _pairwise_score_accuracy(indices, exact, scores)
            exact_pair_correct += correct
            exact_pair_total += total
    labels = np.asarray([float(_action_value(row) > 0.0) for row in rows], dtype=np.float32)
    return {
        "auc": round(_binary_auc(scores, labels), 6),
        "positive_action_rate": round(float(labels.mean()), 6) if len(labels) else 0.0,
        "positive_states": positive_states,
        "top1_positive_state_hit_rate": _rate(top1_positive, positive_states),
        "top5_positive_state_hit_rate": _rate(top5_positive, positive_states),
        "mean_best_positive_rank": _mean(best_positive_ranks),
        "median_best_positive_rank": _median(best_positive_ranks),
        "pairwise_positive_state_accuracy": _rate_float(positive_pair_correct, positive_pair_total),
        "exact_states": exact_states,
        "top1_exact_state_hit_rate": _rate(top1_exact, exact_states),
        "top5_exact_state_hit_rate": _rate(top5_exact, exact_states),
        "mean_best_exact_rank": _mean(best_exact_ranks),
        "median_best_exact_rank": _median(best_exact_ranks),
        "pairwise_exact_state_accuracy": _rate_float(exact_pair_correct, exact_pair_total),
    }


def _feature_vector(row: dict[str, Any], schema: dict[str, Any]) -> list[float]:
    out: list[float] = []
    for field in schema.get("categorical_fields") or []:
        value = _feature_field(row, field)
        categories = (schema.get("categories") or {}).get(field) or []
        out.extend(1.0 if value == category else 0.0 for category in categories)
    parent = str(row.get("parent_mol") or "")
    reactants = [str(item) for item in row.get("reactants") or [] if item]
    n_bits = int(schema.get("n_bits") or 128)
    out.extend(_fp(parent, n_bits=n_bits).tolist())
    out.extend(_fp(".".join(reactants), n_bits=n_bits).tolist())
    context_features = dict(row.get("context_features") or {})
    if row.get("active_failure_modes") and not context_features.get("active_failure_modes"):
        context_features["active_failure_modes"] = row.get("active_failure_modes")
    numeric = _numeric_features(
        parent_mol=parent,
        reactants=reactants,
        candidate_index=row.get("candidate_index"),
        parent_depth=row.get("parent_depth"),
        base_score=row.get("base_score"),
        base_cost=row.get("base_cost"),
        cascade_adjustment=row.get("cascade_adjustment"),
        rule_total_cost=row.get("total_cost"),
        components=row.get("components") or {},
        source_model=row.get("source_model"),
        reaction_domain=row.get("reaction_domain"),
        route_domain=row.get("route_domain") or context_features.get("route_domain"),
        context_features=context_features,
        source_policy_decision=row.get("source_policy_decision") or {},
    )
    out.extend(float(numeric.get(name, 0.0)) for name in schema.get("numeric_fields") or [])
    return out


def _numeric_features(
    *,
    parent_mol: str,
    reactants: list[str],
    candidate_index: Any,
    parent_depth: Any,
    base_score: Any,
    base_cost: Any,
    cascade_adjustment: Any,
    rule_total_cost: Any,
    components: dict[str, Any],
    source_model: Any = None,
    reaction_domain: Any = None,
    route_domain: Any = None,
    context_features: dict[str, Any] | None = None,
    source_policy_decision: dict[str, Any] | None = None,
) -> dict[str, float]:
    parent_desc = _mol_desc(parent_mol)
    reactant_descs = [_mol_desc(smiles) for smiles in reactants]
    reactant_total_heavy = sum(item["heavy_atoms_raw"] for item in reactant_descs)
    parent_heavy = parent_desc["heavy_atoms_raw"]
    values = {
        "parent_depth": _scaled(parent_depth, 8.0),
        "candidate_index": _scaled(candidate_index, 100.0),
        "base_score": _bounded(base_score, 0.0, 1.0),
        "base_cost": _scaled(base_cost, 12.0),
        "cascade_adjustment": _signed_scaled(cascade_adjustment, 6.0),
        "rule_total_cost": _scaled(rule_total_cost, 12.0),
        "reactant_count": min(1.0, len(reactants) / 6.0),
        "parent_heavy_atoms": min(1.0, parent_desc["heavy_atoms_raw"] / 80.0),
        "parent_hetero_atoms": min(1.0, parent_desc["hetero_atoms_raw"] / 30.0),
        "parent_ring_count": min(1.0, parent_desc["ring_count_raw"] / 10.0),
        "parent_mol_wt": min(1.0, parent_desc["mol_wt_raw"] / 1000.0),
        "reactant_total_heavy_atoms": min(1.0, reactant_total_heavy / 160.0),
        "reactant_total_hetero_atoms": min(1.0, sum(item["hetero_atoms_raw"] for item in reactant_descs) / 60.0),
        "reactant_total_ring_count": min(1.0, sum(item["ring_count_raw"] for item in reactant_descs) / 20.0),
        "reactant_total_mol_wt": min(1.0, sum(item["mol_wt_raw"] for item in reactant_descs) / 2000.0),
        "reactant_max_mol_wt": min(1.0, max([item["mol_wt_raw"] for item in reactant_descs] or [0.0]) / 1000.0),
        "heavy_atom_balance": min(2.0, reactant_total_heavy / max(parent_heavy, 1.0)) / 2.0,
    }
    values.update(
        _process_context_features(
            source_model=source_model,
            reaction_domain=reaction_domain,
            route_domain=route_domain,
            parent_depth=parent_depth,
            context_features=context_features or {},
            source_policy_decision=source_policy_decision or {},
        )
    )
    for name in COMPONENT_FIELDS:
        values[f"component_{name}"] = _signed_scaled(components.get(name), 6.0)
    return values


def _feature_field(row: dict[str, Any], field: str) -> str:
    if field == "adjacent_reaction_domain":
        return str((row.get("context_features") or {}).get(field) or "unknown")
    if field == "route_domain":
        return str(row.get("route_domain") or (row.get("context_features") or {}).get(field) or "unknown")
    return str(row.get(field) or "unknown")


def _process_context_features(
    *,
    source_model: Any,
    reaction_domain: Any,
    route_domain: Any,
    parent_depth: Any,
    context_features: dict[str, Any],
    source_policy_decision: dict[str, Any],
) -> dict[str, float]:
    context = context_features if isinstance(context_features, dict) else {}
    policy = source_policy_decision if isinstance(source_policy_decision, dict) else {}
    source = str(source_model or "")
    reaction = _normalize(reaction_domain)
    route = _normalize(route_domain or context.get("route_domain"))
    adjacent = _normalize(context.get("adjacent_reaction_domain"))
    preferred_domains = _tokens(context.get("preferred_reaction_domains"))
    discouraged_domains = _tokens(context.get("discouraged_reaction_domains"))
    active_failures = _tokens(context.get("active_failure_modes"))
    selected_models = {str(item) for item in (policy.get("select_models") or []) if item}
    topk_by_model = policy.get("topk_by_model") if isinstance(policy.get("topk_by_model"), dict) else {}
    source_scores = (
        policy.get("source_value_scores")
        if isinstance(policy.get("source_value_scores"), dict)
        else {}
    )
    topk_value = _float_or_none(topk_by_model.get(source)) if topk_by_model else None
    topk_values = [
        float(value)
        for value in (_float_or_none(value) for value in topk_by_model.values())
        if value is not None and value > 0.0
    ]
    max_topk = max(topk_values) if topk_values else None
    score_value = _float_or_none(source_scores.get(source)) if source_scores else None
    score_values = [
        float(value)
        for value in (_float_or_none(value) for value in source_scores.values())
        if value is not None
    ]
    max_score = max(score_values) if score_values else None
    min_score = min(score_values) if score_values else None
    has_adjacent = adjacent not in {"", "unknown", "none", "null"}
    known_reaction = reaction not in {"", "unknown", "none", "null"}
    route_alignment = _route_domain_alignment(route, reaction)
    return {
        "context_node_depth": _scaled(context.get("node_depth", parent_depth), 8.0),
        "route_domain_alignment": route_alignment,
        "preferred_domain_match": float(bool(preferred_domains and reaction in preferred_domains)),
        "unpreferred_domain_with_preference": float(bool(preferred_domains and reaction not in preferred_domains)),
        "discouraged_domain_match": float(bool(discouraged_domains and reaction in discouraged_domains)),
        "has_adjacent_step": float(has_adjacent),
        "adjacent_domain_match": float(bool(has_adjacent and known_reaction and adjacent == reaction)),
        "adjacent_domain_switch": float(bool(has_adjacent and known_reaction and adjacent != reaction)),
        "enzymatic_after_chemical_context": float(adjacent == "chemical" and reaction == "enzymatic"),
        "chemical_after_enzymatic_context": float(adjacent == "enzymatic" and reaction == "chemical"),
        "active_failure_count": min(1.0, len(active_failures) / 5.0),
        "failure_candidate_missing": float("candidatemissing" in active_failures),
        "failure_stock_dead_end": float("stockdeadend" in active_failures),
        "failure_condition_conflict": float("conditionconflict" in active_failures),
        "failure_cofactor_debt": float("cofactordebt" in active_failures),
        "failure_enzyme_evidence_weak": float("enzymeevidenceweak" in active_failures),
        "failure_stage_over_complex": float("stageovercomplex" in active_failures),
        "failure_route_order_mismatch": float("routeordermismatch" in active_failures),
        "failure_low_plausibility": float("lowplausibility" in active_failures),
        "source_policy_has_decision": float(bool(policy)),
        "source_policy_selected": float(bool(selected_models and source in selected_models)),
        "source_policy_topk_fraction": (
            max(0.0, min(1.0, float(topk_value) / float(max_topk)))
            if topk_value is not None and max_topk
            else 0.0
        ),
        "source_policy_top_budget": float(bool(topk_value is not None and max_topk and topk_value >= max_topk)),
        "source_policy_score": _bounded(score_value, 0.0, 1.0) if score_value is not None else 0.0,
        "source_policy_score_ratio": (
            max(0.0, min(1.0, float(score_value) / float(max_score)))
            if score_value is not None and max_score and max_score > 0.0
            else 0.0
        ),
        "source_policy_best_source": float(bool(score_value is not None and max_score is not None and score_value >= max_score)),
        "source_policy_worst_source": float(
            bool(
                score_value is not None
                and min_score is not None
                and max_score is not None
                and max_score > min_score
                and score_value <= min_score
            )
        ),
    }


def _route_domain_alignment(route_domain: str, reaction_domain: str) -> float:
    if not route_domain or not reaction_domain or reaction_domain == "unknown":
        return 0.0
    if route_domain == "all_chemical":
        return float(reaction_domain == "chemical")
    if route_domain in {"all_enzymatic", "whole_cell_biocatalytic"}:
        return float(reaction_domain == "enzymatic")
    if route_domain in {"chemoenzymatic", "hybrid_mimetic"}:
        return float(reaction_domain in {"chemical", "enzymatic"})
    return 0.0


def _state_pair_indices(rows: list[dict[str, Any]]) -> list[tuple[int, int]]:
    by_state: dict[str, list[int]] = defaultdict(list)
    for idx, row in enumerate(rows):
        by_state[str(row.get("state_id") or idx)].append(idx)
    pairs = []
    for indices in by_state.values():
        positives = [idx for idx in indices if _action_value(rows[idx]) > 0.0]
        negatives = [idx for idx in indices if _action_value(rows[idx]) <= 0.0]
        for pos in positives:
            for neg in negatives:
                pairs.append((pos, neg))
    return pairs


def _split_indices_by_role(rows: list[dict[str, Any]]) -> tuple[list[int], list[int], list[int]]:
    splits = {str(row.get("split") or "") for row in rows}
    if {"train", "val", "test"}.issubset(splits):
        train_idx = [idx for idx, row in enumerate(rows) if row.get("split") == "train"]
        val_idx = [idx for idx, row in enumerate(rows) if row.get("split") == "val"]
        test_idx = [idx for idx, row in enumerate(rows) if row.get("split") == "test"]
        if train_idx and val_idx and test_idx:
            return train_idx, val_idx, test_idx
    if "train" in splits and "val" in splits:
        train_idx = [idx for idx, row in enumerate(rows) if row.get("split") == "train"]
        val_idx = [idx for idx, row in enumerate(rows) if row.get("split") == "val"]
        if train_idx and val_idx:
            return train_idx, val_idx, []
    targets = sorted({str(row.get("target_smiles") or "") for row in rows if row.get("target_smiles")})
    n_val = max(1, int(round(len(targets) * 0.2))) if targets else 0
    val_targets = set(targets[::max(1, len(targets) // max(n_val, 1))][:n_val]) if n_val else set()
    train_idx = []
    val_idx = []
    for idx, row in enumerate(rows):
        target = str(row.get("target_smiles") or "")
        (val_idx if target in val_targets else train_idx).append(idx)
    if train_idx and val_idx:
        return train_idx, val_idx, []
    pivot = max(1, int(len(rows) * 0.8))
    return list(range(pivot)), list(range(pivot, len(rows))), []


def _pairwise_score_accuracy(indices: list[int], positives: list[int], scores: np.ndarray) -> tuple[float, int]:
    positive_set = set(positives)
    negatives = [idx for idx in indices if idx not in positive_set]
    correct = 0.0
    total = 0
    for pos in positives:
        for neg in negatives:
            total += 1
            correct += float(scores[pos] > scores[neg]) + 0.5 * float(scores[pos] == scores[neg])
    return correct, total


def _binary_auc(scores: np.ndarray, labels: np.ndarray) -> float:
    positives = scores[labels > 0]
    negatives = scores[labels <= 0]
    if len(positives) == 0 or len(negatives) == 0:
        return 0.5
    total = 0.0
    for score in positives:
        total += float(np.sum(score > negatives))
        total += 0.5 * float(np.sum(score == negatives))
    return total / float(len(positives) * len(negatives))


def _fp(smiles: str, *, n_bits: int) -> np.ndarray:
    arr = np.zeros(n_bits, dtype=np.float32)
    mol = Chem.MolFromSmiles(smiles or "")
    if mol is None:
        return arr
    bv = AllChem.GetMorganFingerprintAsBitVect(mol, 2, nBits=n_bits)
    DataStructs.ConvertToNumpyArray(bv, arr)
    return arr


def _mol_desc(smiles: str) -> dict[str, float]:
    mol = Chem.MolFromSmiles(smiles or "")
    if mol is None:
        return {
            "heavy_atoms_raw": 0.0,
            "hetero_atoms_raw": 0.0,
            "ring_count_raw": 0.0,
            "mol_wt_raw": 0.0,
        }
    return {
        "heavy_atoms_raw": float(mol.GetNumHeavyAtoms()),
        "hetero_atoms_raw": float(sum(1 for atom in mol.GetAtoms() if atom.GetAtomicNum() not in (1, 6))),
        "ring_count_raw": float(mol.GetRingInfo().NumRings()),
        "mol_wt_raw": float(Descriptors.MolWt(mol)),
    }


