def _validate_selection_metric(metric: str) -> None:
    allowed = {
        "val_loss",
        "auc",
        "top1_positive_state_hit_rate",
        "top5_positive_state_hit_rate",
        "pairwise_positive_state_accuracy",
    }
    if metric not in allowed:
        raise ValueError(f"unsupported selection_metric: {metric}")


def _selection_score(metric: str, val_loss: float, val_metrics: dict[str, Any]) -> float:
    if metric == "val_loss":
        return float(val_loss)
    value = val_metrics.get(metric)
    if value is None:
        return float("-inf")
    return float(value)


def _is_better_selection(metric: str, score: float, best_score: float | None) -> bool:
    if best_score is None:
        return True
    if metric == "val_loss":
        return score < best_score
    return score > best_score


def _bounded(value: Any, low: float, high: float) -> float:
    try:
        out = float(value)
    except (TypeError, ValueError):
        return 0.0
    if not math.isfinite(out):
        return 0.0
    return max(low, min(high, out))


def _scaled(value: Any, scale: float) -> float:
    try:
        out = float(value)
    except (TypeError, ValueError):
        return 0.0
    if not math.isfinite(out):
        return 0.0
    return max(0.0, min(1.0, out / scale))


def _signed_scaled(value: Any, scale: float) -> float:
    try:
        out = float(value)
    except (TypeError, ValueError):
        return 0.0
    if not math.isfinite(out):
        return 0.0
    return max(-1.0, min(1.0, out / scale))


def _float_or_none(value: Any) -> float | None:
    try:
        out = float(value)
    except (TypeError, ValueError):
        return None
    if not math.isfinite(out):
        return None
    return out


def _tokens(value: Any) -> set[str]:
    if value is None:
        return set()
    if isinstance(value, str):
        values = [value]
    elif isinstance(value, (list, tuple, set)):
        values = list(value)
    else:
        values = [str(value)]
    return {_normalize(item) for item in values if _normalize(item)}


def _normalize(value: Any) -> str:
    return str(value or "").strip().lower()


def _rate(num: int | float, den: int | float) -> float | None:
    if not den:
        return None
    return round(float(num) / float(den), 6)


def _rate_float(num: float, den: int) -> float | None:
    return _rate(num, den)


def _mean(values: list[int | float]) -> float | None:
    if not values:
        return None
    return round(float(sum(values)) / len(values), 6)


def _median(values: list[int | float]) -> float | None:
    if not values:
        return None
    return round(float(np.median(np.asarray(values, dtype=np.float32))), 6)


